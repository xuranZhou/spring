package com.pcitc.demo.pojo;


public class Students {

  private long studentsId;
  private String studentsName;
  private String studentsGender;
  private long studentsNumber;


  public long getStudentsId() {
    return studentsId;
  }

  public void setStudentsId(long studentsId) {
    this.studentsId = studentsId;
  }


  public String getStudentsName() {
    return studentsName;
  }

  public void setStudentsName(String studentsName) {
    this.studentsName = studentsName;
  }


  public String getStudentsGender() {
    return studentsGender;
  }

  public void setStudentsGender(String studentsGender) {
    this.studentsGender = studentsGender;
  }


  public long getStudentsNumber() {
    return studentsNumber;
  }

  public void setStudentsNumber(long studentsNumber) {
    this.studentsNumber = studentsNumber;
  }

}
